public class Staff {
    private int COUNT=10;
    private int id;

    public Staff(int id) {
        this.id = id;
    }

    public int getCount() {
        return COUNT;
    }

    public void setCount(int COUNT) {
        this.COUNT = COUNT;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
